#!/usr/bin/ruby
filename = ARGV[0]
asciiVal = 0

def steps_char(steps)
  steps > 0 ? '+' : '-'
end

if filename && File.exists?(filename) && File.readable?(filename)
  IO.read(filename).each_byte do |asciiTarget|
    steps    = asciiTarget - asciiVal
    asciiVal = asciiTarget
    print steps_char(steps) * steps.abs
    print "."
  end
else
  print "+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++.++++++++++++++++++++++++++++++++++++++++++++.++++++.---------.--------.--------------------------------------------------------------------.++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++.+.+++++.------------------------------------------------------------------------------------.++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++.+++.--------------.+++.--------------------------------------------------------------------.+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++.+++++.++.+++++.-.------------------------------------------------------------------------------------.++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++.+++.+++.-------.---------------------------------------------------------------------.+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++.+++++.------------------------------------------------------------------------------.+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++.+.--.---------.+++++.-------.-------------------.+++++++++++++++++++++++++++.---------------------------------------------.--------------------.++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++.----------------.------------------------------------------------------------------.++++++++++++++++++++++++++.------------------.------------------------------."
end
