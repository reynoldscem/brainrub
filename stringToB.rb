#!/usr/bin/ruby
asciiVal = 0
filename = ARGV[0]

if !filename.nil? && File.exists?(filename) && File.readable?(filename)
  IO.read(ARGV[0]).split(//).map {|char| char.ord}.each do |asciiTarget|
    steps    = asciiTarget - asciiVal
    asciiVal = asciiTarget
    until steps.zero?
      if steps > 0
        print "+"
        steps -= 1
      else 
        print "-"
        steps += 1
      end
    end
    print "."
  end
else
  print "+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++.++++++++++++++++++++++++++++++++++++++++++++.++++++.---------.--------.--------------------------------------------------------------------.++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++.+.+++++.------------------------------------------------------------------------------------.++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++.+++.--------------.+++.--------------------------------------------------------------------.+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++.+++++.++.+++++.-.------------------------------------------------------------------------------------.++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++.+++.+++.-------.---------------------------------------------------------------------.+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++.+++++.------------------------------------------------------------------------------.+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++.+.--.---------.+++++.-------.-------------------.+++++++++++++++++++++++++++.---------------------------------------------.--------------------.++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++.----------------.------------------------------------------------------------------.++++++++++++++++++++++++++.------------------.------------------------------."
end
